function quoteIdentifier(value: string) {
  if (!/^[a-z][a-z0-9_]*$/.test(value)) {
    throw new Error(`Unsafe SQL identifier in Supabase seed: ${value}`)
  }

  return `"${value}"`
}

function buildSupabaseSeedSql(tables: Record<string, Record<string, unknown>[]>) {
  const configuredTables = new Set<string>(tableInsertOrder)
  const missingTables = Object.keys(tables).filter((table) => !configuredTables.has(table))
  if (missingTables.length > 0) {
    throw new Error(`Missing seed insert order for tables: ${missingTables.join(', ')}`)
  }

  const statements = tableInsertOrder.flatMap((table) => {
    const rows = tables[table] ?? []
    if (rows.length === 0) return []

    const columns = [...new Set(rows.flatMap((row) => Object.keys(row)))].sort()
    const quotedColumns = columns.map(quoteIdentifier).join(', ')
    const json = JSON.stringify(rows)
    if (json.includes('$seed$')) {
      throw new Error(`Seed JSON for ${table} contains the reserved SQL delimiter $seed$.`)
    }

    return [
      `insert into public.${quoteIdentifier(table)} (${quotedColumns})`,
      `select ${quotedColumns}`,
      `from jsonb_populate_recordset(null::public.${quoteIdentifier(table)}, $seed$${json}$seed$::jsonb)`,
      'on conflict do nothing;',
    ].join('\n')
  })

  return [
    '-- Generated by pnpm seed:export. Do not edit by hand.',
    'begin;',
    ...statements,
    'commit;',
    '',
  ].join('\n\n')
}
