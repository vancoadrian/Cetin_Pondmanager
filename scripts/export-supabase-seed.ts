if (!validation.ok) {
  console.error('Seed export validation failed:')
  for (const message of validation.messages) {
    console.error(`- ${message}`)
  }
  process.exitCode = 1
}
else {
  await mkdir(dirname(jsonOutputPath), { recursive: true })
  await writeFile(jsonOutputPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8')
  await writeFile(sqlOutputPath, buildSupabaseSeedSql(payload.tables), 'utf8')

  const totalRows = Object.values(payload.metadata.counts).reduce((sum, count) => sum + count, 0)
  console.log(`Supabase JSON seed export written to ${jsonOutputPath}`)
  console.log(`Supabase SQL seed export written to ${sqlOutputPath}`)
  console.log(`Tables: ${Object.keys(payload.tables).length}, rows: ${totalRows}`)
}
